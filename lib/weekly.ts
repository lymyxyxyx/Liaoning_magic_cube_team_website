function result(
  rank: number,
  playerName: string,
  level: string,
  grade: string,
  average: number,
  personalBest: number,
  attempts: WeeklyAttempt[],
  options: { ageGroup?: string; pbRefreshed?: boolean } = {}
): WeeklyResult {
  return {
    rank,
    playerName,
    playerSlug: playerSlugByName.get(playerName) || playerName,
    gender: femalePlayerNames.has(playerName) ? "女" : "男",
    ageGroup: options.ageGroup,
    level,
    grade,
    average,
    personalBest,
    pbRefreshed: options.pbRefreshed,
    attempts
  };
}

export function getSingleBest(attempts: WeeklyAttempt[]) {
  const valid = attempts.filter((attempt): attempt is number => typeof attempt === "number");
  return valid.length > 0 ? Math.min(...valid) : null;
}

